CREATE OR REPLACE TABLE ML.CUSTOMER_FEATURES_SNOWFLAKE AS
SELECT
  c.CUSTOMER_ID,
  c.PROVINCE,
  c.CITY,
  c.CUSTOMER_SEGMENT,
  c.CONSENT_STATUS,
  COUNT(o.ORDER_ID) AS ORDERS_H1,
  COALESCE(SUM(TRY_TO_DECIMAL(o.ORDER_TOTAL_ZAR, 18, 2)), 0) AS REVENUE_H1_ZAR,
  COUNT(DISTINCT oi.CATEGORY) AS DISTINCT_CATEGORIES,
  DATEDIFF('day', MAX(TRY_TO_DATE(o.ORDER_DATE)), TO_DATE('2024-06-30')) AS RECENCY_DAYS,
  COALESCE(SUM(IFF(LOWER(o.IS_PAID_ATTRIBUTED) = 'true', 1, 0)), 0) AS PAID_ATTRIBUTED_ORDERS
FROM ANALYTICS.CUSTOMERS c
LEFT JOIN ANALYTICS.ORDERS o ON c.CUSTOMER_ID = o.CUSTOMER_ID
LEFT JOIN ANALYTICS.ORDER_ITEMS oi ON o.ORDER_ID = oi.ORDER_ID
GROUP BY c.CUSTOMER_ID, c.PROVINCE, c.CITY, c.CUSTOMER_SEGMENT, c.CONSENT_STATUS;

CREATE OR REPLACE TABLE ML.CUSTOMER_ML_SCORES_SNOWFLAKE AS
SELECT
  *,
  ROUND(1 / (1 + EXP(-(
    -2.20
    + 0.34 * LEAST(ORDERS_H1, 8)
    + 0.00018 * LEAST(REVENUE_H1_ZAR, 25000)
    + 0.28 * LEAST(DISTINCT_CATEGORIES, 6)
    - 0.018 * LEAST(COALESCE(RECENCY_DAYS, 180), 180)
    + 0.20 * IFF(CONSENT_STATUS = 'granted', 1, 0)
  ))), 4) AS PURCHASE_PROPENSITY_SCORE,
  ROUND(1 / (1 + EXP(-(
    -1.45
    + 0.030 * LEAST(COALESCE(RECENCY_DAYS, 180), 180)
    - 0.26 * LEAST(ORDERS_H1, 8)
    - 0.00010 * LEAST(REVENUE_H1_ZAR, 25000)
    + 0.18 * IFF(CONSENT_STATUS <> 'granted', 1, 0)
  ))), 4) AS CHURN_RISK_SCORE
FROM ML.CUSTOMER_FEATURES_SNOWFLAKE;

CREATE OR REPLACE VIEW ML.VW_HIGH_PROPENSITY_AUDIENCE AS
SELECT CUSTOMER_ID, PROVINCE, CUSTOMER_SEGMENT, ORDERS_H1, REVENUE_H1_ZAR, PURCHASE_PROPENSITY_SCORE
FROM ML.CUSTOMER_ML_SCORES_SNOWFLAKE
WHERE PURCHASE_PROPENSITY_SCORE >= 0.45
ORDER BY PURCHASE_PROPENSITY_SCORE DESC;

CREATE OR REPLACE VIEW ML.VW_CHURN_REACTIVATION_AUDIENCE AS
SELECT CUSTOMER_ID, PROVINCE, CUSTOMER_SEGMENT, RECENCY_DAYS, CHURN_RISK_SCORE
FROM ML.CUSTOMER_ML_SCORES_SNOWFLAKE
WHERE CHURN_RISK_SCORE >= 0.58
ORDER BY CHURN_RISK_SCORE DESC;

CREATE OR REPLACE VIEW ML.VW_ROAS_FORECAST_ACTIONS AS
SELECT
  CHANNEL,
  MONTH,
  TRY_TO_DECIMAL(ACTUAL_ROAS, 8, 2) AS ACTUAL_ROAS,
  TRY_TO_DECIMAL(FORECAST_ROAS, 8, 2) AS FORECAST_ROAS,
  CASE
    WHEN TRY_TO_DECIMAL(FORECAST_ROAS, 8, 2) >= 4.0 THEN 'Scale budget'
    WHEN TRY_TO_DECIMAL(FORECAST_ROAS, 8, 2) >= 3.0 THEN 'Hold and test creative'
    ELSE 'Reduce waste and rebuild audience'
  END AS RECOMMENDED_ACTION
FROM ML.CHANNEL_ROAS_FORECAST;

CREATE OR REPLACE VIEW ML.ML_KPI_SCORECARD AS
SELECT
  (SELECT COUNT(*) FROM ML.CUSTOMER_ML_SCORES_SNOWFLAKE) AS SCORED_CUSTOMERS,
  (SELECT COUNT(*) FROM ML.VW_HIGH_PROPENSITY_AUDIENCE) AS HIGH_OR_WARM_PROPENSITY_CUSTOMERS,
  (SELECT COUNT(*) FROM ML.VW_CHURN_REACTIVATION_AUDIENCE) AS CHURN_REACTIVATION_CUSTOMERS,
  (SELECT COUNT(*) FROM ML.VW_ROAS_FORECAST_ACTIONS WHERE RECOMMENDED_ACTION = 'Scale budget') AS SCALE_BUDGET_CHANNEL_MONTHS;
