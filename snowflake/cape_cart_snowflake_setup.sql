CREATE OR REPLACE VIEW ANALYTICS.VW_CAMPAIGN_EXECUTIVE_SCORECARD AS
SELECT
  'Cape & Cart H1 Growth Campaign' AS CASE_STUDY,
  (SELECT COUNT(*) FROM RAW.GA4_EVENTS_RAW) AS GA4_EVENTS,
  (SELECT COUNT(*) FROM RAW.GOOGLE_ADS_RAW) AS GOOGLE_ADS_ROWS,
  (SELECT COUNT(*) FROM RAW.YOUTUBE_RAW) AS YOUTUBE_ROWS,
  (SELECT COUNT(*) FROM RAW.ECOM_ORDERS_RAW) AS RAW_ECOM_ORDERS,
  (SELECT COUNT(*) FROM SILVER.TRANSACTIONAL_QUARANTINE) AS ECOM_QUARANTINED_ROWS,
  (SELECT COUNT(*) FROM ANALYTICS.ORDERS) AS ORDERS_H1,
  (SELECT ROUND(SUM(TRY_TO_DECIMAL(ORDER_TOTAL_ZAR, 18, 2)), 2) FROM ANALYTICS.ORDERS) AS REVENUE_ZAR,
  (SELECT COUNT(*) FROM ANALYTICS.CUSTOMERS) AS CUSTOMERS,
  (SELECT COUNT(*) FROM ANALYTICS.PRODUCTS) AS PRODUCTS;

CREATE OR REPLACE VIEW ANALYTICS.VW_CHANNEL_PERFORMANCE AS
SELECT
  ATTRIBUTION_CHANNEL,
  COUNT(*) AS ORDERS,
  ROUND(SUM(TRY_TO_DECIMAL(ORDER_TOTAL_ZAR, 18, 2)), 2) AS REVENUE_ZAR,
  ROUND(AVG(TRY_TO_DECIMAL(ORDER_TOTAL_ZAR, 18, 2)), 2) AS AOV_ZAR,
  SUM(IFF(LOWER(IS_PAID_ATTRIBUTED) = 'true', 1, 0)) AS PAID_ATTRIBUTED_ORDERS
FROM ANALYTICS.ORDERS
GROUP BY ATTRIBUTION_CHANNEL;

